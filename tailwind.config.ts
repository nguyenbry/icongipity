function generateRadixColorScale(name: string) {
  const scale = Array.from({ length: 12 }, (_, i) => {
    const id = i + 1;
    return [id, `var(--${name}${id})`];
  });

  return Object.fromEntries(scale) as RadixColorScale;
}
